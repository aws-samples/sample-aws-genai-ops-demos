import { useSplitPanel } from '../SplitPanelContext';
import { getStatusConfig, getAlertConfig, PRIORITY_COLORS } from '../constants';

function GenAiLabel() {
  return (
    <Box variant="awsui-gen-ai-label" fontSize="body-s">
      <Icon name="gen-ai" size="small" />
      Generated by AI
    </Box>
  );
}

interface FunctionRecord {
  function_arn: string;
  region: string;
  runtime: string;
  target_runtime?: string;
  alert_status?: string;
  migration_complexity?: string;
  migration_status?: string;
  priority_score?: number;
  tags?: Record<string, string>;
  invocation_count_14d?: number;
  avg_daily_invokes?: number;
  last_invocation_date?: string;
  deprecation_date?: string;
  days_to_deprecation?: number;
  package_type?: string;
  memory_size?: number;
  timeout?: number;
  handler?: string;
  code_size?: number;
  architectures?: string[];
  layers?: string[];
  env_vars?: Record<string, string>;
  ta_last_updated?: string;
  [key: string]: unknown;
}

type SortingColumn = { sortingField: string };

function extractFunctionName(arn: string): string {
  const match = arn.match(/function:(.+)/);
  return match ? match[1] : arn;
}

function formatDate(dateStr?: string): string {
  if (!dateStr) return '—';
  try {
    return new Date(dateStr).toLocaleDateString(undefined, {
      year: 'numeric', month: 'short', day: 'numeric',
    });
  } catch { return dateStr; }
}

function formatBytes(bytes?: number): string {
  if (!bytes) return '—';
  if (bytes < 1024) return `${bytes} B`;
  if (bytes < 1024 * 1024) return `${(bytes / 1024).toFixed(1)} KB`;
  return `${(bytes / (1024 * 1024)).toFixed(1)} MB`;
}

